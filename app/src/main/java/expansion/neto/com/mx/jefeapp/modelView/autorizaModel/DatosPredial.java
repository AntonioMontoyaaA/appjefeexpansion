package expansion.neto.com.mx.jefeapp.modelView.autorizaModel;

public class DatosPredial {

    String aplicaPredial;
    String codigo;
    String mensaje;

    public String getAplicaPredial() {
        return aplicaPredial;
    }

    public void setAplicaPredial(String aplicaPredial) {
        this.aplicaPredial = aplicaPredial;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }
}
